async function main() {
  console.log("🚀 بدء إدخال الوظائف الحقيقية...\n");

  let created = 0;
  let errors = 0;

  for (const job of JOBS) {
    try {
      await prisma.job.create({
        data: {
          slug: slug(job.title),
          title: job.title,
          companyNameText: job.companyNameText,
          companyRelation: "CURATED_EXTERNAL",
          city: job.city,
          area: job.area || null,
          jobCategory: job.jobCategory,
          jobType: job.jobType,
          salaryMin: job.salaryMin || null,
          salaryMax: job.salaryMax || null,
          salaryText: job.salaryText || null,
          description: job.description,
          responsibilities: job.responsibilities || null,
          requirements: job.requirements || null,
          benefits: job.benefits || null,
          experienceLevel: job.experienceLevel || "NO_EXPERIENCE",
          educationLevel: job.educationLevel || "NONE",
          skills: job.skills || null,
          womenFriendly: job.womenFriendly || false,
          noExperienceRequired: job.noExperienceRequired || false,
          requiresDrivingLicense: job.requiresDrivingLicense || false,
          contactMethod: job.contactMethod || "INTERNAL_APPLY",
          contactWhatsapp: job.contactWhatsapp || null,
          contactEmail: job.contactEmail || null,
          externalUrl: job.externalUrl || null,
          sourceType: "ADMIN_MANUAL",
          sourceName: "إدارة جوبز الأردن",
          source: "admin-seed",
          isVerified: true,
          sourceVerifiedAt: now,
          sourceTrustLevel: "HIGH",
          status: "PUBLISHED",
          publishedAt: now,
          expiresAt: expires,
          featured: false,
          urgent: false,
        },
      });
      created++;
      console.log(`  ✅ ${created}. ${job.title} — ${job.city}`);
    } catch (err) {
      errors++;
      console.error(`  ❌ خطأ في "${job.title}":`, err.message);
    }
  }

  console.log(`\n═══════════════════════════════════`);
  console.log(`✅ تم إنشاء ${created} وظيفة بنجاح`);
  if (errors > 0) console.log(`❌ ${errors} أخطاء`);
  console.log(`═══════════════════════════════════\n`);

  const total = await prisma.job.count({ where: { status: "PUBLISHED" } });
  console.log(`📊 إجمالي الوظائف المنشورة في قاعدة البيانات: ${total}`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
